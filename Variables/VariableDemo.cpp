/*
Program Name : VariableDemo.cpp
Concept      : Variables
Input        : None
Output       : Displays variable values
Logic        : Declare variables, assign values, print them
*/
#include <iostream>
using namespace std;

int main() {
    int age = 16;
    float height = 5.8;
    char grade = 'A';

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}
